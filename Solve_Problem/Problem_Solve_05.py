# Problem 5: The Armstrong Number
# Description:
# Write a function is_armstrong(num) that checks if a given number num is an Armstrong number. 
# An Armstrong number is a number that is equal to the sum of its own digits, each raised to the power of the number of digits.
# Examples:
# •	is_armstrong(153) should return True, because it has 3 digits, and 13+53+33=1+125+27=153.
# •	is_armstrong(1634) should return True, because it has 4 digits, and 14+64+34+44=1+1296+81+256=1634.



